    private void moveDadosTelaParaObjeto(){
        proprietario = new Proprietario();
        proprietario.setCodProprietario(Integer.parseInt(txtCodigo.getText()));
        proprietario.setNome(txtNome.getText());
        proprietario.setCodProprietario(Integer.parseInt(txtCodigo.getText()));
        
        try {
            proprietario = proDAO.buscaID(proprietario);
            
            if(proprietario != null){ //achou
                   moveDadosObjetoParaTela();
            }
            else{
                mensagem("Proprietario não encontrado!");
            }
        } 
        catch (SQLException ex) {
            mensagem("Erro de Busca: " + ex.getMessage());
        }
    }
      private void moveDadosObjetoParaTela(){
          txtCodigo.setText(String.valueOf(proprietario.getCodProprietario()));
          txtNome.setText(proprietario.getNome());
        
        
        // Vai movimentar os dados do objeto proprietario para a tela
    }
      
      
