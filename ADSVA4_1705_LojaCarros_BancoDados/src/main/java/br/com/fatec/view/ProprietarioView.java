    private void btnBucarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBucarActionPerformed
        proprietario = new Proprietario();
        proprietario.setCodProprietario(Integer.parseInt(txtCodigo.getText()));
        
        try{
            proprietario = proDAO.buscaID(proprietario);
            if(proprietario != null){//achou
                moveDadosObjetoParaTela();
            } else{
                mensagem("Proprietario não encontrado!!");
            }
                
        } catch(SQLException ex){
            mensagem("Erro de Busca: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_btnBucarActionPerformed

